    cam_rows = ""
    for cam in cameras:
        cam_rows += f"""
        <tr>
          <td><strong>{cam['ip']}</strong></td>
          <td>{cam['brand']}</td>
          <td>{', '.join(map(str, cam['open_ports']))}</td>
          <td>{'<span style="color:#dc2626">YES</span>' if cam['rtsp_available'] else 'No'}</td>
          <td>{'YES' if cam['has_web_interface'] else 'No'}</td>
        </tr>"""

    vuln_rows = ""
    for v in vulns:
        color  = sev_color.get(v["severity"], "#6b7280")
        status = "VULNERABLE" if v["status"] == "FAIL" else ("PROTECTED" if v["status"] == "PASS" else "INFO")
        s_col  = "#dc2626" if v["status"] == "FAIL" else "#16a34a"
        vuln_rows += f"""
        <tr>
          <td><span style="color:{s_col};font-weight:bold">{status}</span></td>
          <td><span style="background:{color};color:white;padding:2px 8px;border-radius:12px;font-size:0.75rem">{v['severity']}</span></td>
          <td><strong>{v['title']}</strong></td>
          <td>{v['description']}</td>
        </tr>"""

    pass_status = "CRITICAL — Default credentials FOUND!" if passwords.get("vulnerable") else "PASS — No default credentials found"
