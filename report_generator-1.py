"""
Module 4: Report Generator
- Professional HTML + TXT security reports
- SOC-ready format
"""

import os
from datetime import datetime

def generate_report(target, network_cameras, vuln_results, password_results, risk_score):
    os.makedirs("reports", exist_ok=True)
    timestamp = datetime.now().strftime("%Y%m%d_%H%M%S")
    txt_path  = f"reports/cctvguard_report_{timestamp}.txt"
    html_path = f"reports/cctvguard_report_{timestamp}.html"

    _write_txt(txt_path, target, network_cameras, vuln_results, password_results, risk_score, timestamp)
    _write_html(html_path, target, network_cameras, vuln_results, password_results, risk_score, timestamp)

    return html_path, txt_path


def _risk_level(score):
    if score >= 30: return "CRITICAL RISK"
    if score >= 20: return "HIGH RISK"
    if score >= 10: return "MEDIUM RISK"
    return "LOW RISK"


def _write_txt(path, target, cameras, vulns, passwords, score, ts):
    level = _risk_level(score)
    lines = [
        "=" * 70,
        "           CCTVGuard — SECURITY ASSESSMENT REPORT",
        "=" * 70,
        f"Target      : {target}",
        f"Date        : {ts}",
        f"Risk Score  : {score}/100",
        f"Risk Level  : {level}",
        "=" * 70,
        "",
        "[ 1. CAMERAS DISCOVERED ]",
        "-" * 40,
    ]
    for cam in cameras:
        lines.append(f"  IP: {cam['ip']} | Brand: {cam['brand']} | Ports: {cam['open_ports']}")
        lines.append(f"  RTSP: {'YES' if cam['rtsp_available'] else 'NO'} | Web: {'YES' if cam['has_web_interface'] else 'NO'}")
        lines.append("")

    lines += ["", "[ 2. VULNERABILITY FINDINGS ]", "-" * 40]
    for v in vulns:
        status = "VULNERABLE" if v["status"] == "FAIL" else "PROTECTED"
        lines.append(f"  [{v['severity']}] {v['title']} — {status}")
        lines.append(f"    {v['description']}")
        lines.append("")

    lines += ["", "[ 3. PASSWORD SCAN RESULTS ]", "-" * 40]
    if passwords.get("vulnerable"):
        lines.append(f"  [CRITICAL] Default credentials FOUND!")
        for cred in passwords.get("credentials_found", []):
            lines.append(f"    Credentials: {cred}")
    else:
        lines.append("  [PASS] No default credentials found")

    lines += [
        "", "[ 4. RECOMMENDATIONS ]", "-" * 40,
        "  1. Change default credentials immediately",
        "  2. Update firmware to latest version",
        "  3. Disable Telnet and unused services",
        "  4. Enable HTTPS — disable HTTP",
        "  5. Use VPN for remote camera access",
        "  6. Segment cameras on separate VLAN",
        "  7. Disable UPnP to prevent internet exposure",
        "  8. Enable motion detection alerts",
        "  9. Regularly audit camera access logs",
        " 10. Apply patches for known CVEs immediately",
        "",
        "=" * 70,
        "  Generated by CCTVGuard v1.0 — SOC Portfolio Tool",
        "  github.com/muhammed95rafi-arch",
        "=" * 70,
    ]
    with open(path, "w") as f:
        f.write("\n".join(lines))


def _write_html(path, target, cameras, vulns, passwords, score, ts):
    level = _risk_level(score)
    risk_color = "#dc2626" if score >= 30 else "#ea580c" if score >= 20 else "#ca8a04"
